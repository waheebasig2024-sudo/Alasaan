export const GREETINGS = {
  morning: "صباح النور",
  afternoon: "مرحبا",
  evening: "مساء الخير",
  night: "مساء النور",
};

const GREETING_VARIATIONS = [
  (time: string, name: string) => `${time}${name}! زين، وش تبغى؟`,
  (time: string, name: string) => `${time}${name}! أنا هنا، كيف أساعدك؟`,
  (time: string, name: string) => `${time}${name}! خير، ايش في خاطرك؟`,
  (time: string, name: string) => `${time}${name}! تفضل، الحسن في خدمتك.`,
];

export function getGreeting(userName?: string): string {
  const hour = new Date().getHours();
  let time = GREETINGS.morning;
  if (hour >= 12 && hour < 17) time = GREETINGS.afternoon;
  else if (hour >= 17 && hour < 20) time = GREETINGS.evening;
  else if (hour >= 20) time = GREETINGS.night;

  const name = userName ? ` يا ${userName}` : "";
  const pick = GREETING_VARIATIONS[Math.floor(Math.random() * GREETING_VARIATIONS.length)];
  return pick(time, name);
}
